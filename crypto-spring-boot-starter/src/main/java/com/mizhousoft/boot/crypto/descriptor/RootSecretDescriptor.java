package com.mizhousoft.boot.crypto.descriptor;

/**
 * 根密钥描述符
 *
 * @version
 */
public class RootSecretDescriptor
{
	private String dog;

	private String pig;

	private String cat;

	private String bird;

	/**
	 * 获取dog
	 * 
	 * @return
	 */
	public String getDog()
	{
		return dog;
	}

	/**
	 * 设置dog
	 * 
	 * @param dog
	 */
	public void setDog(String dog)
	{
		this.dog = dog;
	}

	/**
	 * 获取pig
	 * 
	 * @return
	 */
	public String getPig()
	{
		return pig;
	}

	/**
	 * 设置pig
	 * 
	 * @param pig
	 */
	public void setPig(String pig)
	{
		this.pig = pig;
	}

	/**
	 * 获取cat
	 * 
	 * @return
	 */
	public String getCat()
	{
		return cat;
	}

	/**
	 * 设置cat
	 * 
	 * @param cat
	 */
	public void setCat(String cat)
	{
		this.cat = cat;
	}

	/**
	 * 获取bird
	 * 
	 * @return
	 */
	public String getBird()
	{
		return bird;
	}

	/**
	 * 设置bird
	 * 
	 * @param bird
	 */
	public void setBird(String bird)
	{
		this.bird = bird;
	}
}
